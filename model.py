import numpy as np
from sklearn.preprocessing import MinMaxScaler
from sklearn.metrics import mean_squared_error
from sklearn.linear_model import LinearRegression


# -----------------------------------
# PREPARE DATA
# -----------------------------------
def prepare_lstm_data(data, time_step=60):

    data = data.dropna()

    data = data.values.reshape(-1, 1)

    scaler = MinMaxScaler(feature_range=(0, 1))
    scaled = scaler.fit_transform(data)

    X, y = [], []

    for i in range(time_step, len(scaled)):
        X.append(scaled[i-time_step:i, 0])
        y.append(scaled[i, 0])

    X = np.array(X)
    y = np.array(y)

    # reshape for regression
    X = X.reshape(X.shape[0], X.shape[1])

    return X, y, scaler


# -----------------------------------
# TRAIN MODEL (Linear Regression)
# -----------------------------------
def train_lstm(X, y):

    split = int(0.8 * len(X))

    X_train, X_test = X[:split], X[split:]
    y_train, y_test = y[:split], y[split:]

    model = LinearRegression()

    model.fit(X_train, y_train)

    preds = model.predict(X_test)

    rmse = np.sqrt(mean_squared_error(y_test, preds))

    return model, rmse


# -----------------------------------
# NEXT DAY PRICE PREDICTION
# -----------------------------------
def predict_next_price(model, data, scaler):

    data = data.dropna()

    last_60 = data.values[-60:].reshape(-1, 1)

    last_60 = scaler.transform(last_60)

    last_60 = last_60.reshape(1, 60)

    prediction = model.predict(last_60)

    prediction = scaler.inverse_transform(prediction.reshape(-1,1))

    return float(prediction[0][0])


# -----------------------------------
# TRADE SIGNAL
# -----------------------------------
def generate_trade_signal(current_price, predicted_price, threshold=0.5):

    if predicted_price > current_price + threshold:
        return "BUY 📈"

    elif predicted_price < current_price - threshold:
        return "SELL 📉"

    else:
        return "HOLD ⏸"
