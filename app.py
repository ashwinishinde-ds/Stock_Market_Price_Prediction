import streamlit as st
import yfinance as yf
import matplotlib.pyplot as plt


from model import (
    prepare_lstm_data,
    train_lstm,
    predict_next_price,
    generate_trade_signal
)

st.set_page_config(page_title="Live Indian Stock Trade Signal", layout="centered")

st.title("📊 Live Indian Stock Market – Next Trade Signal")
st.caption("Educational Decision Support System (Not Financial Advice)")

# -----------------------------------
# STOCK SELECTION
# -----------------------------------
stock = st.selectbox(
    "Select Indian Stock (NSE)",
    ["TCS.NS", "INFY.NS", "RELIANCE.NS", "HDFCBANK.NS", "ICICIBANK.NS"]
)

if st.button("Get Next Trade Signal"):

    with st.spinner("Fetching live market data & training model..."):

        # Fetch near-real-time NSE data
        # Fetch near-real-time NSE data
        data = yf.download(stock, period="1y", interval="1d")

        # ✅ FIX: flatten MultiIndex columns from yfinance
        data.columns = [col[0] if isinstance(col, tuple) else col for col in data.columns]

        # ✅ Reset index to get Date column
        data = data.reset_index()

        # ✅ Remove missing values
        data = data.dropna()

        # ✅ Separate data for model & graph
        close_data = data[['Close']]              # for MODEL
        close_data_plot = data[['Date', 'Close']] # for GRAPH



        # Prepare LSTM data
        X, y, scaler = prepare_lstm_data(close_data)

        # Train model
        model, rmse = train_lstm(X, y)

       # Predict next price
        predicted_price = predict_next_price(model, close_data, scaler)

        # Current market price
        current_price = close_data.iloc[-1, 0]

        # Generate trade signal
        signal = generate_trade_signal(current_price, predicted_price)

    # -----------------------------------
    # DISPLAY RESULTS
    # -----------------------------------
    st.success("Analysis Completed")

    st.subheader("📌 Market Prices")
    st.write(f"*Current Price:* ₹{current_price:.2f}")
    st.write(f"*Predicted Next Price:* ₹{predicted_price:.2f}")

    st.subheader("🧠 Suggested Next Action")
    st.write(f"### {signal}")

    st.subheader("📉 Model Performance")
    st.write(f"RMSE: *{rmse:.4f}*")

    st.subheader("📈Price Trend (Last 1 Year)")
    st.line_chart(close_data_plot.set_index('Date'))



    st.warning(
        "⚠ This application is strictly for educational and analytical purposes. "
        "It does NOT provide financial or investment advice."
    )
